    public void cargarComboProducto(){
        Gestor g = new Gestor();
//        ArrayList <Producto> lista = g.mostrarProductos();
        ArrayList producto = g.mostrarProductos();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (Object p : producto) {
            modelo.addElement(p);
        }
        
        cmbProductos.setModel(modelo);
    }

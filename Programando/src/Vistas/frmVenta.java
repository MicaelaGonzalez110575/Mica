    private void btnAceptarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarVActionPerformed
        String cliente = txtCliente.getText();
        int cantidad = Integer.parseInt(txtCantidad.getText());
        Producto producto = (Producto) cmbProductos.getSelectedItem();
        
        Gestor g = new Gestor();
        
        Venta v = new Venta();
        v.setCliente(cliente);
        v.setCantidad(cantidad);
        v.setProducto(producto);
        g.agregarVenta(v);
        
        
    }//GEN-LAST:event_btnAceptarVActionPerformed


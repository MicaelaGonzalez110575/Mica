    private void btnAgregarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarMActionPerformed
       DBGestor gestor = new DBGestor();
        
       String mascota = txtMascota.getText();
       Especie especie= (Especie) cboEspecie.getSelectedItem();
       Cliente cliente = (Cliente)cboCliente.getSelectedItem();
       int nac = Integer.parseInt(txtNac.getText());
       
        Mascota m = new Mascota();
        m.setNombre(mascota);
        m.setCliente(cliente);
        m.setEspecie(especie);
        m.setNac(nac);
       
        gestor.agregarMascota(m);
    }//GEN-LAST:event_btnAgregarMActionPerformed


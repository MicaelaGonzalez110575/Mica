    private void cargarComboCliente(){
        DBGestor g = new DBGestor();
        ArrayList lista = g.obtenerCliente();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        for (Object c : lista) {
            modelo.addElement(c);
        }
        cboCliente.setModel(modelo);
    }
